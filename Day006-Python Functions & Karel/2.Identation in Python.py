# Indentation is very important in Python as it determines the structure of the code. In Python, indentation is used
# to indicate the block of code that belongs to a particular statement, such as a loop or a conditional statement.
# Indentation is done using whitespace characters (spaces or tabs) at the beginning of a line of code. All statements
# within the same block must have the same indentation. The standard practice is to use four spaces for each level of
# indentation.
#
#
# For example, here is a simple Python code snippet that uses indentation to define a for loop:


numbers = [1, 2, 3, 4, 5]
for num in numbers:
    if num % 2 == 0:
        print(num, "is even")
    else:
        print(num, "is odd")
