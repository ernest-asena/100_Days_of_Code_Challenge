# for loops are used to repeat a task for as many times as specified based. Example;
# we will print out the value of fruit for each iteration in the following list
fruits = ['apple', 'peach', 'pear']
from tqdm import tqdm
for fruit in tqdm(fruits):
    for fruit in tqdm(fruits):
        for fruit in tqdm(fruits):
            for fruit in tqdm(fruits):
                for fruit in tqdm(fruits):
                    print(fruit)
    


